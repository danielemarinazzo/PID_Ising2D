function f=FunMI(v,pp)
Z(1:8,1)=[-0.30559711  0.39574033  0.30559711 -0.39574033  0.30559711 -0.39574033 -0.30559711  0.39574033];
Z(1:8,2)=[-0.39574033 -0.30559711  0.39574033  0.30559711  0.39574033  0.30559711 -0.39574033 -0.30559711];
% Z =[  -0.3056   -0.3957;
%     0.3957   -0.3056;
%     0.3056    0.3957;
%    -0.3957    0.3056;
%     0.3056    0.3957;
%    -0.3957    0.3056;
%    -0.3056   -0.3957;
%     0.3957   -0.3056];





p=pp+Z*v;
p1(1:2)=0.5;
p123=reshape(p,2,2,2);
p23=squeeze(sum(p123));

f=0;
if isempty(find(p<0));
    for s1=1:2
        for s2=1:2
            for s3=1:2
                f=f+p123(s1,s2,s3)*log(p123(s1,s2,s3)/(p1(s1)*p23(s2,s3)));
            end
        end
    end
else
    f=inf;
end